#FUA para calcular a área de um triângulo, exibindo o resultado. 
# A base e a altura são dados que devem ser lidos como entrada. 
base= int(input('Digite a medida da base de um triângulo:'))
altura= int(input('Digite a medida da altura de um triângulo:'))
area= int(base * altura) / 2
print(f'A área do triângulo é {area}')


